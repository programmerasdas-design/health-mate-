def load_users():
    file_name = "users.json"
    if os.path.exists(file_name):
        with open(file_name, "r") as f:
            return json.load(f)
    return {}

def save_users(users):
    with open("users.json", "w") as f:
        json.dump(users, f)

def validate_password(password):
    if len(password) != 8:
        print("The password must contain eight characters")
        return False
    if not re.search(r"[A-Z]", password):
        print("The password must contain at least one capital letter")
        return False
    if not re.search(r"[a-z]", password):
        print("The password must contain at least one lowercase letter")
        return False
    if not password.isalpha():
        print("The password must not contain symbols or numbers (letters only)")
        return False
    return True

