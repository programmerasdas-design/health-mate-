def analyze_symptoms(user_input):
    user_symptoms = [symptom.strip().lower() for symptom in user_input.split(",")]
    results = {}

    for disease, disease_symptoms in disease_db.items():
        match_count = 0
        for symptom in user_symptoms:
            if symptom in [s.lower() for s in disease_symptoms]:
                match_count += 1

        if match_count > 0:
            match_percentage = (match_count / len(disease_symptoms)) * 100
            results[disease] = round(match_percentage, 2)

    return results

